package com.basic;

public class Arithmatic {

	public static void main(String[] args) {
		
		int a=20,b=30;
		
		int add=a+b;
		int sub=a-b;
		int mul=a*b;
		int div=a/b;
		int mod=a%b;
		 
		System.out.println("Addition:"+add+" Subtraction:"+sub+" Multiplication:"+mul+
				" Division:"+div+" Modulus:"+mod);

	}

}
