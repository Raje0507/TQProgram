package AssignmentNO2;

public class EG6 {

	public static void main(String[] args) {
		int l=4;
		int m=1;
		for(int i=1;i<=7;i+=2)
		{

			for(int k=1;k<=l;k++)
			{
				System.out.print(" ");
			}
			for(int j=1;j<=i;j++)
			{
				System.out.print(m);
				
			}
			System.out.println();
			l--;
			m++;
		}
		

	}

}
